package com.service.google.places;

public class PlacesEngineException extends Exception {

	private static final long serialVersionUID = -8218416339194113740L;

	public PlacesEngineException() {
		super();
	}

	public PlacesEngineException(String arg0) {
		super(arg0);
	}

	public PlacesEngineException(Throwable arg0) {
		super(arg0);
	}

	public PlacesEngineException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}