package com.service.google.places;

public class GooPlacesException extends Exception {

	private static final long serialVersionUID = -8218416339194113740L;

	public GooPlacesException() {
		super();
	}

	public GooPlacesException(String arg0) {
		super(arg0);
	}

	public GooPlacesException(Throwable arg0) {
		super(arg0);
	}

	public GooPlacesException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}